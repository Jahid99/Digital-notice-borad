In today’s world, everything is digitalized and paper is being used less and less every day. How often has it happened that we miss some important notice because we have to go to a wall and read the notice there? There are dedicated file hosting sites and clouds used by some institutions, but there is a definite need for a dedicated noticeboard system. The proposed system is such a system.   
A Notice Board is a place where people can leave public messages, for example, to advertise things to buy or sell, announce events, or provide information. Notice boards are often made of a material such as cork to facilitate addition and removal of paper messages or it can be placed on digital devices such as computers, phones so people can leave and erase messages for other people to read and see. The main aim of this research work is to make information dissemination much easier in a paperless community as the world tends to graduate into that line of interaction. To develop a Digital Notice Board facility as a software for use in University’s Administration. This research work is about the implementation of a DNB in a university system. This research work generally intends to act as a support system for the existing method by which notices are being posted in the University environment.
      
